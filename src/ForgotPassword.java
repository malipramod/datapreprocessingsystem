    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String userName = jTextField1.getText();
        String securityQuestion = "";
        //  ConnectDatabase con=new ConnectDatabase();
        if (userName.equals("")) {
            JOptionPane.showMessageDialog(this, "User Name is missing", "User Name", JOptionPane.WARNING_MESSAGE);
            
        } else {
            ConnectDatabase con = new ConnectDatabase();
            securityQuestion = con.forgetPasswor(userName);
            if (securityQuestion.equals("")) {
                jLabel2.setText(securityQuestion);
                jTextField2.setEnabled(false);
                jButton1.setEnabled(false);
                JOptionPane.showMessageDialog(this, "No User Name Found", "Invalide User Name", JOptionPane.WARNING_MESSAGE);
            } else {
                jLabel2.setText(securityQuestion);
                jTextField2.setEnabled(true);
                jButton1.setEnabled(true);
                jButton2.setEnabled(false);
                jButton3.setEnabled(true);
                jTextField1.setEnabled(false);
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Provide Answer", "Answer missing", JOptionPane.WARNING_MESSAGE);
        }
        ConnectDatabase con = new ConnectDatabase();
        if (con.varifySecurityAnswer(jTextField1.getText()).equals(jTextField2.getText())) {
           jTextField3.setEnabled(true);
           jTextField4.setEnabled(true);
           jButton4.setEnabled(true);
           jButton1.setEnabled(false);
           jTextField2.setEnabled(false);
           jButton5.setEnabled(true);
        }
        else
        {
               JOptionPane.showMessageDialog(this, "Answer Didn't Match", "Invalide Answer", JOptionPane.WARNING_MESSAGE);
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

