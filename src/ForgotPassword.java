    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
      String newPassword="";
      String userName=jTextField1.getText();
      if(!(jTextField3.getText().equals(jTextField4.getText())))
      {
          JOptionPane.showMessageDialog(this, "Password didn't matched", "Password change failed", JOptionPane.ERROR_MESSAGE);
      }
      else
      {
          newPassword=jTextField3.getText();
          ConnectDatabase con=new ConnectDatabase();          
          if(con.changePassword(newPassword, userName))
          {
               JOptionPane.showMessageDialog(this, "Password Changed Successfully", "Success", JOptionPane.WARNING_MESSAGE);
          }
      }
    }//GEN-LAST:event_jButton4ActionPerformed

